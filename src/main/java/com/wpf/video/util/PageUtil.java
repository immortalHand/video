package com.wpf.video.util;

public class PageUtil {

    private int page;

    private int list;

    private int pages;

    private Object infos;

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getList() {
        return list;
    }

    public void setList(int list) {
        this.list = list;
    }

    public int getPages() {
        return pages;
    }

    public void setPages(int pages) {
        this.pages = pages;
    }

    public Object getInfos() {
        return infos;
    }

    public void setInfos(Object infos) {
        this.infos = infos;
    }
}
